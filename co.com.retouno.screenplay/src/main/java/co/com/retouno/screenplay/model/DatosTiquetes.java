package co.com.retouno.screenplay.model;

public class DatosTiquetes {
	
	private String origen;
	private String destino;
	private String fechaida;
	private String fecharegreso;
	private String moneda;
	private String numeropasajero;
	private String numeroninio;
	private String numeroinfante;
	public String getOrigen() {
		return origen;
	}
	public void setOrigen(String origen) {
		this.origen = origen;
	}
	public String getDestino() {
		return destino;
	}
	public void setDestino(String destino) {
		this.destino = destino;
	}
	public String getFechaida() {
		return fechaida;
	}
	public void setFechaida(String fechaida) {
		this.fechaida = fechaida;
	}
	public String getFecharegreso() {
		return fecharegreso;
	}
	public void setFecharegreso(String fecharegreso) {
		this.fecharegreso = fecharegreso;
	}
	public String getMoneda() {
		return moneda;
	}
	public void setMoneda(String moneda) {
		this.moneda = moneda;
	}
	public String getNumeropasajero() {
		return numeropasajero;
	}
	public void setNumeropasajero(String numeropasajero) {
		this.numeropasajero = numeropasajero;
	}
	public String getNumeroninio() {
		return numeroninio;
	}
	public void setNumeroninio(String numeroninio) {
		this.numeroninio = numeroninio;
	}
	public String getNumeroinfante() {
		return numeroinfante;
	}
	public void setNumeroinfante(String numeroinfante) {
		this.numeroinfante = numeroinfante;
	}
	
	

}
